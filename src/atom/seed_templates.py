"""Seed templates: 60 session templates (20 × 3 levels).

Each template has a topic and curated segment pool.
Segments are shuffled per round at runtime — the list here
defines WHICH combos appear and HOW OFTEN (via repetition).
"""

from __future__ import annotations

import random

CUES = ["가드!", "좋아!", "더 빠르게!", "스텝!", "호흡!", "턱 당겨!", "강하게!", "힘 빼!", "눈!"]


def _t(
    name: str,
    level: str,
    topic: str,
    combos: list[tuple[str, int]],
    n_cues: int,
    seed: int,
    cue_pool: list[str] | None = None,
) -> dict:
    """Build a template dict from weighted combos.

    Args:
        combos: [(combo_call, repeat_count), ...] — how many times each combo appears.
        n_cues: number of cue entries to add.
        seed: deterministic seed for cue selection.
        cue_pool: override default CUES for topic-specific cue emphasis.
    """
    rng = random.Random(seed)
    segs: list[str] = []
    for combo, count in combos:
        segs.extend([combo] * count)
    pool = cue_pool or CUES
    segs.extend(rng.choices(pool, k=n_cues))
    return {
        "name": name,
        "level": level,
        "topic": topic,
        "segments_json": {
            "intro": ["시작합니다"],
            "segments": segs,
            "outro": ["수고했어!"],
        },
    }


